// Get a random item from selected categories
export function getRandomAdhkar(categories = ['general', 'duaa'], excludeIndex = -1) {
  const pool = [];
  categories.forEach((cat) => {
    if (ADHKAR_CONTENT[cat]) {
      ADHKAR_CONTENT[cat].forEach((item, i) => {
        pool.push({ ...item, category: cat, globalIndex: `${cat}-${i}` });
      });
    }
  });

  if (pool.length === 0) return null;
  if (pool.length === 1) return pool[0];

  // Try to avoid the last sent index
  let candidate;
  let attempts = 0;
  do {
    candidate = pool[Math.floor(Math.random() * pool.length)];
    attempts++;
  } while (candidate.globalIndex === excludeIndex && attempts < 5);

  return candidate;
}

export function getCategoryStats() {
  const stats = {};
  for (const [cat, items] of Object.entries(ADHKAR_CONTENT)) {
    stats[cat] = items.length;
  }
  return stats;
}
