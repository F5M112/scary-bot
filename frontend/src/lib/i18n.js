export const useI18n = create(
  persist(
    (set, get) => ({
      lang: 'ar',
      setLang: (lang) => {
        set({ lang });
        if (typeof document !== 'undefined') {
          document.documentElement.lang = lang;
          document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        }
      },
      toggleLang: () => {
        const newLang = get().lang === 'ar' ? 'en' : 'ar';
        get().setLang(newLang);
      },
      t: (key) => {
        const lang = get().lang;
        return translations[lang]?.[key] || translations.ar[key] || key;
      },
    }),
    { name: 'app-language' }
  )
);

// Helper hook to get just `t`
export const useT = () => {
  const lang = useI18n((s) => s.lang);
  return (key) => translations[lang]?.[key] || translations.ar[key] || key;
};
